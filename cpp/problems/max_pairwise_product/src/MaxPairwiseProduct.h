/**
* Find the maximum number in a sequence.
**/
int findMax(int numbers[], int size);

/**
* Finds the maximum number of a sequence but excluding a specific value.
**/
int findMaxExcept(int numbers[], int size, int* except);

/**
* Calculates the maximum pairwise product of the numbers in an array.
**/
long long MaxPairwiseProduct(int numbers[], int size);
